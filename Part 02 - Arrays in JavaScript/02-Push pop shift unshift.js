let fruits = [
    "Apple",
    "Banana",
    "Orange",
    "Strawberry",
    "Mango",
    "Pineapple",
    "Watermelon",
    "Grape",
    "Kiwi",
    "Peach"
];

// let's understand push,pop
console.log(fruits)
fruits.push("Pineapple"); // insert into the last of array
console.log(fruits);
fruits.pop(); // remove element into the last of the array


// let's undertand shift and unshift 
fruits.shift("Pineapple");
console.log(fruits);
fruits.unshift();
console.log(fruits);